/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
    int mask = ~0;
    int y = x ^ (x<<1);
    int count = 0;
    count = ((!!(y & (mask << 16))) << 4);
    count += ((!!(y & (mask << (count+8)))) << 3);
    count += ((!!(y & (mask << (count+4)))) << 2);
    count += ((!!(y & (mask << (count+2)))) << 1);
    count += (!!(y & (mask << (count+1))));
    return count+1;
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
    int mask = 0x1<<31;
    int a = x + ~y + 1;
    int result = !(a & mask);

    int flag1 = !(x & mask);
    int flag2 = !(y & mask);

    int flag3 = flag1 +1 + ~flag2;

    flag1 = !flag3&((!a)|(!result));
    flag2 = flag3&!!(flag3^0x1);

    return flag1|flag2;
}/*
 * intLog2 - return floor(log base 2 of x), where x > 0
 *   Example: intLog2(16) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 90
 *   Rating: 4
 */
int intLog2(int x) {
    int mask = ~0;
    int n = ((!!(x & (mask << 16))) << 4);
    n += ((!!(x & (mask << (n + 8)))) << 3);
    n += ((!!(x & (mask << (n + 4)))) << 2);
    n += ((!!(x & (mask << (n + 2)))) << 1);
    n += (!!(x & (mask << (n + 1))));
    return n;
}
